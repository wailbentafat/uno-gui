package model.enums;

/**
 * Enum representing different actions in a card game.
 */
public enum ActionType {
    /**
     * Action to draw two cards from the deck.
     */
    DRAW_2,

    /**
     * Action to reverse the direction of play.
     */
    REVERSE,

    /**
     * Action to skip the next player's turn.
     */
    SKIP
}
