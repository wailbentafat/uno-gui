package model.enums;

/**
 * Enum representing different colors of cards in a card game.
 */
public enum Color {
    /**
     * Represents the color red.
     */
    RED,

    /**
     * Represents the color yellow.
     */
    YELLOW,

    /**
     * Represents the color blue.
     */
    BLUE,

    /**
     * Represents the color green.
     */
    GREEN,

    /**
     * Represents the absence of a specific color.
     */
    NONE
}
